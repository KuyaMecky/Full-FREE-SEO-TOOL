#!/usr/bin/env node
// Concatenates all Prisma migrations into a single SQL file you can pipe
// into a fresh Turso database:
//
//   node scripts/build-turso-schema.mjs
//   turso db shell <your-db-name> < prisma/turso-schema.sql
//
// This is a one-off bootstrap — use whenever you create a new Turso DB.
// For incremental migrations after that, apply only the new migration files.

import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const migrationsDir = join(process.cwd(), "prisma", "migrations");
const outPath = join(process.cwd(), "prisma", "turso-schema.sql");

const dirs = readdirSync(migrationsDir, { withFileTypes: true })
  .filter((e) => e.isDirectory())
  .map((e) => e.name)
  .sort();

const chunks = [
  "-- Generated by scripts/build-turso-schema.mjs",
  "-- DO NOT commit edits directly — update the Prisma migrations and re-run.\n",
];

for (const dir of dirs) {
  const sqlPath = join(migrationsDir, dir, "migration.sql");
  try {
    const sql = readFileSync(sqlPath, "utf8");
    chunks.push(`-- ─── ${dir} ───`);
    chunks.push(sql.trim());
    chunks.push("");
  } catch {
    // migration.sql missing — skip
  }
}

writeFileSync(outPath, chunks.join("\n") + "\n", "utf8");
console.log(`Wrote ${outPath}`);
console.log(`Bundled ${dirs.length} migrations.`);
